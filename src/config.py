# ใช้ค่าความน่าจะเป็น (0-1) ที่โมเดลทำนาย มาแบ่งระดับ
RISK_LEVELS = [
    {"name": "ต่ำ",      "color": "#2ecc71", "min_prob": 0.00, "max_prob": 0.20},
    {"name": "ปานกลาง", "color": "#f1c40f", "min_prob": 0.20, "max_prob": 0.40},
    {"name": "สูง",      "color": "#e67e22", "min_prob": 0.40, "max_prob": 0.70},
    {"name": "วิกฤต",    "color": "#e74c3c", "min_prob": 0.70, "max_prob": 1.01},
]


def get_risk_level(probability: float) -> dict:
    """
    แปลงค่าความน่าจะเป็น (0-1) เป็นระดับความเสี่ยง
    คืนค่า dict ที่มี name และ color
    """
    for level in RISK_LEVELS:
        if level["min_prob"] <= probability < level["max_prob"]:
            return level
    return RISK_LEVELS[-1]   # กรณี probability = 1.0 พอดี


